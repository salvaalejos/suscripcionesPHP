    private void btnAddSucursal1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddSucursal1ActionPerformed
        // TODO add your handling code here:
        try {
            String title = fieldTitle.getText();
            String description = fieldDescription.getText();
            Double price = (double) fieldPrice.getValue();
            Sucursal sucursal = (Sucursal) sucursalSelector.getSelectedItem();
            Integer idSucursal = sucursal.getIdSucursal();

            if(title.isEmpty() || description.isEmpty()){
                return;
            }

            SubscriptionPlan subscription = new SubscriptionPlan(null, price, title, description, idSucursal, true);
            addSubscription(subscription);
            fieldTitle.setText("");
            fieldDescription.setText("");
            fieldPrice.setValue(1.0);
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnAddSucursal1ActionPerformed

    private void btnChangeStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeStatusActionPerformed
        // TODO add your handling code here:

        try {
            SubscriptionPlan subscription = selectedSubscription();
            subscription.setStatus(!subscription.isStatus());
            modelSubscriptionPlan.update(subscription);
            readSubscriptions();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnChangeStatusActionPerformed

    private SubscriptionPlan selectedSubscription(){
        try {
            int indice = jTable1.getSelectedRow();
            if(indice<0){
                return null;
            }
            int idSubscription =  (int)jTable1.getValueAt(indice, 0);
            SubscriptionPlan subscription = modelSubscriptionPlan.byId(idSubscription);

            return subscription;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }
    

