    private void readSubscriptions(){
        subscriptions.clear();

        try {
            subscriptions = modelSubscriptionPlan.getAll();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            for(SubscriptionPlan subscription : subscriptions){

                String estado = "";
                if(subscription.isStatus()){
                    estado = "Activa";
                } else{
                    estado = "Inactiva";
                }
                
                model.addRow(new Object[]{subscription.getIdSubscriptionPlan(), subscription.getTitle(),
                    subscription.getDescription(), subscription.getPrice(), estado});
                
            }
            jTable1.setModel(model);
            jTable1.updateUI();

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void addSubscription(SubscriptionPlan subscription){
        try {
            modelSubscriptionPlan.add(subscription);
            readSubscriptions();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    private void readSucursales(){
        sucursales.clear();
        sucursalSelector.removeAll();
        try {
            sucursales = modelSucursal.getActives();

            for(Sucursal sucursal : sucursales){
                sucursalSelector.addItem(sucursal);
                
            }
            sucursalSelector.updateUI();


        } catch (Exception e) {
            e.printStackTrace();
        }
    }

