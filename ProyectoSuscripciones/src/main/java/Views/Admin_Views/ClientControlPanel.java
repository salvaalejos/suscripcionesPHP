    private void readClients(){
        clients.clear();

        try {
            clients = modelUser.getClients();
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            for(User client : clients){
                String estado = "";
                if(client.isStatus()){
                    estado = "Activa";
                } else{
                    estado = "Inactiva";
                }

                Sucursal s = modelSucursal.byId(client.getSucursal());
                String sucursal = s.getNombre();

                Subscription sub = modelSubscription.byUser(client.getId_user());
                SubscriptionPlan plan = modelSubscriptionPlan.byId(sub.getSubscriptionPlan());
                String subscription = plan.getTitle();

                if(plan == null){
                    subscription = "No tiene suscripcion aún";
                    estado = "Inactiva";
                }

                model.addRow(new Object[]{client.getId_user(), client.getUsername(), client.getPhone(), client.getEmail(), sucursal, subscription, estado});
                jTable1.setModel(model);
                jTable1.updateUI();
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    private void readSucursales(){
        sucursales.clear();
        sucursalSelector.removeAll();
        try {
            sucursales = modelSucursal.getActives();

            for(Sucursal sucursal : sucursales){
                sucursalSelector.addItem(sucursal);
            }
            sucursalSelector.updateUI();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

