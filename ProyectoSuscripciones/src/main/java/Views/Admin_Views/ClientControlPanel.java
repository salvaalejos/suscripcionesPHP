    private void btnChangeSucursalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeSucursalActionPerformed
        // TODO add your handling code here:
        
        Sucursal sucursal = (Sucursal) sucursalSelector.getSelectedItem();
        Integer idSucursal = sucursal.getIdSucursal();

        if(idSucursal == null){
            return;
        }

        User client = selectedClient();
        client.setSucursal(idSucursal);
        try {
            modelUser.update(client);
            JOptionPane.showMessageDialog(null, "Sucursal cambiada");
            readClients();
        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_btnChangeSucursalActionPerformed

    private void btnChangeStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnChangeStatusActionPerformed
        // TODO add your handling code here:
        try {

            User client = selectedClient();
            Subscription sub = modelSubscription.byUser(client.getId_user());
            SubscriptionPlan plan = modelSubscriptionPlan.byId(sub.getSubscriptionPlan());

            if (plan == null) {
                JOptionPane.showMessageDialog(null, "No tiene suscripción aún, por lo que sigue INACTIVO");
                return;
            }

            client.setStatus(!client.isStatus());
            modelUser.update(client);
            JOptionPane.showMessageDialog(null, "Estado cambiado");
            readClients();

        }catch (Exception e) {
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnChangeStatusActionPerformed
    
    private User selectedClient(){
        try {
            User client = null;
            int indice = jTable1.getSelectedRow();
            if(indice<0){
                return null;
            }
            int idUser =  (int)jTable1.getValueAt(indice, 0);
            client = modelUser.byId(idUser);
            return client;
        } catch (Exception e) {
            e.printStackTrace();
        }
        return null;
    }



